package a220107;

public class NextLine {
	public static void main(String[] args) {
//		\n = 계행
		System.out.print("abcd"); 
		System.out.print("\n1234\n");
//		System.out.printf("\n1234"); print든 f든 \n은 가능 . f는 %n도 됨
		System.out.print("abcd"); 
		System.out.println(); // 이렇게도 가능 
		System.out.print("1234");
	}
}
